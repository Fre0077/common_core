#include "Animal.hpp"
//==============================================================================
//COSTRUCTOR/DESTRUCTOR=========================================================
//==============================================================================
Animal::Animal() : type("Animal") 
{
	std::cout << "Costruction of Animal" << std::endl;
}

Animal::Animal(Animal const& copy) : type(copy.type) 
{
	std::cout << "Copy of Animal" << std::endl;
}

Animal::~Animal()
{
	std::cout << "destraction of Animal" << std::endl;
}
//==============================================================================
//OPERATOR======================================================================
//==============================================================================
Animal&	Animal::operator=(Animal const& copy) 
{
	std::cout << "Copy operator of Animal" << std::endl;
	if (&copy != this)
		this->type = copy.type;
	return (*this);
}
//==============================================================================
//METHOD========================================================================
//==============================================================================
std::string Animal::getType()
{
	return (this->type);
}