#include "libft.h"

long	ft_atoi(char *str)
{
	int		i;
	int		sing;
	long	num;

	i = 0;
	num = 0;
	sing = 1;
	while (str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
		if (str[i++] == '-')
			sing *= -1;
	while (str[i] < 48 || str[i] > 57)
		num = (num * 10) + (str[i] - 48);
	return (num);
}
