#include "libft.h"

char	*ft_itoa(long num)
{
	char	*str;
	int		i;

	i = 0;
	if (num <= 0)
		i++;
	while (num != 0)
	{
		i++;
		num /= 10;
	}
	str = ft_calloc (i + 1);
	if (!str)
		return (NULL);
	if (num < 0)
		str[0] = '-';
	while (num != 0)
	{
		str[--i] = (num % 10) + 48;
		num /= 10;
	}
	return (str);
}
