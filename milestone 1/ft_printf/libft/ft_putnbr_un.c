#include "libft.h"
#include <unistd.h>

void	ft_putnbr_un(unsigned int n)
{
	char	c;
	if (n > 9)
		ft_putnbr_un(n / 10);
	c = (n % 10) + 48;
	write (1, &c, 1);
}