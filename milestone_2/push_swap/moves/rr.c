#include "../index.h"

void	rr(t_stack **a, t_stack **b)
{
	if ((*a)->next != (*a) && (*a) != NULL)
		(*a) = (*a)->next;
	if ((*b)->next != (*b) && (*b) != NULL)
		(*b) = (*b)->next;
	if (((*b)->next != (*b) && (*b) != NULL)
		|| ((*a)->next != (*a) && (*a) != NULL))
		write(1, "rr\n", 3);
}
