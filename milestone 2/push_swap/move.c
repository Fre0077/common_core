#include "index.h"

void	move(t_stack **a, t_stack **b, int s[7])
{
	if (((*a)->next)->val == s[0] || ((*a)->previus)->val == s[1])
	{
		if (s[6] == 0)
			s[6] = 1;
		else 
			s[6] = 0;
	}
	if (s[6] == 0)
	{
		if (s[5] == 1)
		{
			s[5] = 0;
			s[3] = ((*b)->previus)->val;
			rr(a, b);
		}
		else
			ra(a);
	}
	else
		rra(a);
}